public class PointDouble {

    public Double x;
    public Double y;

    public PointDouble() {
    }

    public void set(Double aX, Double aY) {
        x = aX;
        y = aY;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double aX) {
        x = aX;
    }

    public void setY(Double aY) {
        y = aY;
    }

    public Double getY() {
        return y;
    }
}
